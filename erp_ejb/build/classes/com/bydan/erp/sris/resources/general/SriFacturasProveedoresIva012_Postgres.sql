select 
	(select nombre from contabilidad.tipo_comprobante where id=l.id_tipo_comprobante) as nombre_tipo_comprobante,
	(select nombre_completo from cartera.cliente where id=l.id_cliente) as nombre_completo_cliente,
	l.ruc as ruc_dato,
	l.numero_documento,
	l.fecha_emision,
	l.fecha_registro_contable,
	l.numero_serie,
	l.total_iva,
	l.total_sin_iva,
	(select id from contabilidad.tipo_iva where id=l.id_tipo_iva) as id_tipo_iva,
	(select id from contabilidad.tipo_retencion_iva where id=l.id_tipo_retencion_iva) as id_tipo_retencion_iva,
	l.transfe_iva_retenido,
	(select id from contabilidad.tipo_retencion_iva where id=l.id_tipo_retencion_iva_presta) as id_tipo_retencion_iva_presta,
	l.presta_iva_retenido,
	(select nombre from contabilidad.tipo_retencion where id=f.id_tipo_retencion_fuente1) as nombre_tipo_retencion_fuente1,
	f.ret_fuente1_porcentaje,
	f.ret_fuente1_retenido,
	(select nombre from contabilidad.tipo_retencion where id=f.id_tipo_retencion_fuente2) as nombre_tipo_retencion_fuente2,
	f.ret_fuente2_porcentaje,
	f.ret_fuente2_retenido,
	(select nombre from contabilidad.tipo_retencion where id=f.id_tipo_retencion_iva1) as nombre_tipo_retencion_iva1,
	f.ret_iva_bien_porcentaje,
	f.ret_iva_bien_retenido,
	(select nombre from contabilidad.tipo_retencion where id=f.id_tipo_retencion_iva2) as nombre_tipo_retencion_iva2,
	f.ret_iva_servicio_porcentaje,
	f.ret_iva_servicio_retenido
from sris.transaccion_local l
inner join contabilidad.factura_proveedor f on l.numero_documento_contable=f.numero_mayor
--where